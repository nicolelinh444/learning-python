# display square, cube, and fourth of 6, only use ** for fourth

# declare variable to store number
num = 6

# number squared
squared = (num * num)

# number cube
cubed = (num * num * num)

# number to the fourth power
fourthPower = (num ** 4)

print("6 squared is", squared)
print("6 cubed is", cubed)
print("6 to the fourth power is", fourthPower)